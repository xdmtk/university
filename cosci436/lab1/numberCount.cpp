#include <cstdio>
#include <ctime>
#include <cstdlib>



#define MAX_AR_LEN 100


void populateNumArray(int *numbers);




void main(int argc, char * argv[]) {

    // Declare numbers array
    int numbers[MAX_AR_LEN];

    // Populate numbers array
    populateNumArray(numbers);













}



// Function to populate integer array with random numbers
void populateNumArray(int *numbers) {
    

    // Seed random number generator
    srand(time(NULL));
    
    // Iterate through numbers array
    for (int i=0; i < MAX_AR_LEN; ++i ) {

        // Add random number to each element in the array
        numbers[i] = (rand() % 100);
    }

    // Return void since array passed by reference
    return;


}
