    public static String convertToBinaryString(double num) {

        String output = "";

        // Determine max 2^n on num
        double base,n;
        base=2;


        for (n=0; base < num; n++) {
           base=2;
           base += Math.pow(base,n);
        }
        --n;

        for (base=2; n >= 0; n--) {
            if (num/Math.pow(base,n) >= 1) {
                output += "1";
                num -= Math.pow(base,n);
                System.out.println("Base * n: " +  Math.pow(base,n));
            }
            else {
                output += "0";
            }
        }
        return output;

    }





        












